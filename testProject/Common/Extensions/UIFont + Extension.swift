extension UIFont {
    
    static func konchmerNormal32() -> UIFont? {
        return UIFont.init(name: "KonkhmerSleokchher-Regular", size: 32)
    }
    
    static func konchmerNormal16() -> UIFont? {
        return UIFont.init(name: "KonkhmerSleokchher-Regular", size: 16)
    }
    
    static func konchmerNormal12() -> UIFont? {
        return UIFont.init(name: "KonkhmerSleokchher-Regular", size: 12)
    }
}
